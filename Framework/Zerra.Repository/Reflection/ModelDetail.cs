using System.Diagnostics.CodeAnalysis;
using Zerra.Reflection;

namespace Zerra.Repository.Reflection
{
    /// <summary>
    /// Holds reflected metadata about a repository model type, including property lists categorised
    /// by identity, relation, and data-source characteristics.
    /// </summary>
    public sealed class ModelDetail
    {
        /// <summary>
        /// The underlying <see cref="Zerra.Reflection.TypeDetail"/> for this model type.
        /// </summary>
        public TypeDetail TypeDetail { get; }
        /// <summary>
        /// The CLR <see cref="System.Type"/> of this model.
        /// </summary>
        public Type Type { get; }
        /// <summary>
        /// The simple name of the model type.
        /// </summary>
        public string Name { get; }

        /// <summary>
        /// The closed <c>Func&lt;T, bool&gt;</c> delegate type used when building LINQ predicate expressions for this model.
        /// </summary>
        public Type LambdaDelegateType { get; }

        private readonly string? dataSourceEntityName;
        /// <summary>
        /// Gets a value indicating whether this type is decorated with <see cref="EntityAttribute"/>.
        /// </summary>
        public bool IsDataSourceEntity => dataSourceEntityName is not null;
        /// <summary>
        /// The store or table name for this model, as specified by <see cref="EntityAttribute"/> or defaulting to the type name.
        /// </summary>
        /// <exception cref="Exception">Thrown when the type does not have an <see cref="EntityAttribute"/>.</exception>
        public string DataSourceEntityName => dataSourceEntityName ?? throw new Exception($"{Type.Name} does not have an EntityAttribute");
        /// <summary>
        /// All model properties that are not excluded by <see cref="StoreExcludeAttribute"/>.
        /// </summary>
        public IReadOnlyList<ModelPropertyDetail> Properties { get; }
        /// <summary>
        /// Properties marked with <see cref="IdentityAttribute"/>; typically the primary key column(s).
        /// </summary>
        public IReadOnlyList<ModelPropertyDetail> IdentityProperties { get; }
        /// <summary>
        /// Identity properties whose values are auto-generated by the data source (e.g. auto-increment / DB identity).
        /// </summary>
        public IReadOnlyList<ModelPropertyDetail> IdentityAutoGeneratedProperties { get; }
        /// <summary>
        /// Properties marked with <see cref="RelationAttribute"/>; represent foreign-key relationships.
        /// </summary>
        public IReadOnlyList<ModelPropertyDetail> RelatedProperties { get; }
        /// <summary>
        /// Related properties whose type implements <see cref="System.Collections.Generic.IEnumerable{T}"/>; represent one-to-many relationships.
        /// </summary>
        public IReadOnlyList<ModelPropertyDetail> RelatedEnumerableProperties { get; }
        /// <summary>
        /// Related properties whose type does not implement <see cref="System.Collections.Generic.IEnumerable{T}"/>; represent many-to-one or one-to-one relationships.
        /// </summary>
        public IReadOnlyList<ModelPropertyDetail> RelatedNonEnumerableProperties { get; }
        /// <summary>
        /// Properties that are neither auto-generated identity columns nor relation properties.
        /// </summary>
        public IReadOnlyList<ModelPropertyDetail> NonAutoGeneratedNonRelationProperties { get; }

        private readonly IDictionary<string, ModelPropertyDetail> propertiesByName;
        /// <summary>
        /// Returns the <see cref="ModelPropertyDetail"/> whose name exactly matches <paramref name="name"/>.
        /// </summary>
        /// <param name="name">The case-sensitive property name.</param>
        /// <returns>The matching <see cref="ModelPropertyDetail"/>.</returns>
        /// <exception cref="Exception">Thrown when no property with the given name exists.</exception>
        public ModelPropertyDetail GetProperty(string name)
        {
            if (!this.propertiesByName.TryGetValue(name, out var property))
                throw new Exception($"{nameof(ModelDetail)} for {Type.Name} does not contain property {name}");
            return property;
        }
        /// <summary>
        /// Attempts to find a <see cref="ModelPropertyDetail"/> by its exact name.
        /// </summary>
        /// <param name="name">The case-sensitive property name.</param>
        /// <param name="property">When successful, the matching <see cref="ModelPropertyDetail"/>; otherwise <see langword="null"/>.</param>
        /// <returns><see langword="true"/> if the property was found; otherwise <see langword="false"/>.</returns>
        public bool TryGetProperty(string name,
#if !NETSTANDARD2_0
            [MaybeNullWhen(false)]
#endif
        out ModelPropertyDetail property)
        {
            return this.propertiesByName.TryGetValue(name, out property);
        }

        private readonly IDictionary<string, ModelPropertyDetail> propertiesByNameLower;
        /// <summary>
        /// Returns the <see cref="ModelPropertyDetail"/> whose lowercase name matches the lowercase form of <paramref name="name"/>.
        /// </summary>
        /// <param name="name">The property name to look up (case-insensitive).</param>
        /// <returns>The matching <see cref="ModelPropertyDetail"/>.</returns>
        /// <exception cref="Exception">Thrown when no property with the given name exists.</exception>
        public ModelPropertyDetail GetPropertyLower(string name)
        {
            if (!this.propertiesByNameLower.TryGetValue(name, out var property))
                throw new Exception($"{nameof(ModelDetail)} for {Type.Name} does not contain property {name}");
            return property;
        }
        /// <summary>
        /// Attempts to find a <see cref="ModelPropertyDetail"/> by a case-insensitive property name lookup.
        /// </summary>
        /// <param name="name">The property name to look up (case-insensitive).</param>
        /// <param name="property">When successful, the matching <see cref="ModelPropertyDetail"/>; otherwise <see langword="null"/>.</param>
        /// <returns><see langword="true"/> if the property was found; otherwise <see langword="false"/>.</returns>
        public bool TryGetPropertyLower(string name,
#if !NETSTANDARD2_0
            [MaybeNullWhen(false)]
#endif
        out ModelPropertyDetail property)
        {
            return this.propertiesByNameLower.TryGetValue(name, out property);
        }

        /// <summary>
        /// A boxed parameterless factory delegate that creates a new instance of the model type.
        /// </summary>
        public Func<object> Creator { get; set; }

        /// <inheritdoc/>
        public override string ToString()
        {
            return $"{Type.Name} {Name}";
        }

        internal ModelDetail(TypeDetail typeDetail)
        {
            this.TypeDetail = typeDetail;
            this.Type = typeDetail.Type;

            this.LambdaDelegateType = typeof(Func<,>).MakeGenericType(this.Type, typeof(bool));

            if (!typeDetail.HasCreatorBoxed)
                throw new Exception($"{Type.Name} must have a parameterless constructor to be a model.");

            while (typeDetail.InnerTypes.Count == 1)
            {
                this.Type = typeDetail.InnerType!;
                typeDetail = typeDetail.InnerTypeDetail!;
            }

            var sourceEntityAttribute = typeDetail.Attributes.Select(x => x as EntityAttribute).Where(x => x is not null).FirstOrDefault();
            if (sourceEntityAttribute is not null)
            {
                var storeName = sourceEntityAttribute.StoreName;
                if (String.IsNullOrWhiteSpace(storeName))
                    storeName = this.Type.Name;

                if (!storeName.All(x => Char.IsLetterOrDigit(x) || x == '_' || x == '`'))
                    throw new ArgumentException($"{nameof(EntityAttribute)}.{nameof(EntityAttribute.StoreName)}={this.DataSourceEntityName} is not a valid name");
                this.dataSourceEntityName = storeName;
            }

            var modelProperties = new List<ModelPropertyDetail>();
            foreach (var member in typeDetail.Members)
            {
                var notSourcePropertyAttribute = member.Attributes.Select(x => x as StoreExcludeAttribute).Where(x => x is not null).FirstOrDefault();
                if (notSourcePropertyAttribute is null)
                {
                    var modelPropertyInfo = new ModelPropertyDetail(member, this.IsDataSourceEntity);
                    modelProperties.Add(modelPropertyInfo);
                }
            }

            this.Name = typeDetail.Type.Name;
            this.Properties = modelProperties.ToArray();

            this.propertiesByName = this.Properties.ToDictionary(x => x.Name);
            this.propertiesByNameLower = this.Properties.ToDictionary(x => x.Name.ToLower());
            this.IdentityProperties = this.Properties.Where(x => x.IsIdentity).ToArray();
            this.IdentityAutoGeneratedProperties = this.Properties.Where(x => x.IsIdentity && x.IsIdentityAutoGenerated).ToArray();
            this.RelatedProperties = this.Properties.Where(x => x.IsRelated).ToArray();
            this.RelatedEnumerableProperties = this.Properties.Where(x => x.IsRelated && x.IsEnumerable).ToArray();
            this.RelatedNonEnumerableProperties = this.Properties.Where(x => x.IsRelated && !x.IsEnumerable).ToArray();
            this.NonAutoGeneratedNonRelationProperties = this.Properties.Where(x => !x.IsIdentityAutoGenerated && !x.IsRelated).ToArray();

            this.Creator = typeDetail.CreatorBoxed;
        }
    }
}
