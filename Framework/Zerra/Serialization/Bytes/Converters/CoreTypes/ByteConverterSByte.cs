using Zerra.IO;

namespace Zerra.Serialization
{
    internal sealed class ByteConverterSByte<TParent> : ByteConverter<TParent, sbyte>
    {
        protected override bool TryRead(ref ByteReader reader, ref ReadState state, out sbyte value)
        {
            if (!reader.TryReadSByte(out value, out var sizeNeeded))
            {
                state.BytesNeeded = sizeNeeded;
                return false;
            }
            return true;
        }

        protected override bool TryWrite(ref ByteWriter writer, ref WriteState state, sbyte value)
        {
            if (!writer.TryWrite(value, out var sizeNeeded))
            {
                state.BytesNeeded = sizeNeeded;
                return false;
            }
            return true;
        }
    }
}